fn layout_points(results: &[(MaskType, Vec<GeneResult>)]) -> Vec<PlotPoint> {
    let offsets = chrom_offsets();
    let mut points = Vec::new();

    for (_, genes) in results {
        for g in genes {
            let p = g.staar.staar_o;
            if !p.is_finite() || p <= 0.0 || p > 1.0 { continue; }
            let chrom = g.chromosome.strip_prefix("chr").unwrap_or(&g.chromosome);
            let (idx, offset) = match offsets.get(chrom) {
                Some(v) => *v,
                None => continue,
            };
            let midpoint = (g.start as f64 + g.end as f64) / 2.0;
            points.push(PlotPoint {
                x: offset as f64 + midpoint,
                y: -p.log10(),
                chrom_idx: idx,
                label: g.gene_symbol.clone(),
            });
        }
    }
    points
}

fn chrom_offsets() -> HashMap<String, (usize, u64)> {
    let mut map = HashMap::new();
    let mut offset = 0u64;
    for (i, &(name, size)) in CHROMS.iter().enumerate() {
        map.insert(name.to_string(), (i, offset));
        offset += size;
    }
    map
}

fn genome_length() -> f64 {
    CHROMS.iter().map(|(_, s)| *s as f64).sum()
}

fn manhattan_svg(points: &[PlotPoint]) -> String {
    let w = 960.0_f64;
    let h = 300.0_f64;
    let ml = 55.0; let mr = 15.0; let mt = 10.0; let mb = 40.0;
    let pw = w - ml - mr;
    let ph = h - mt - mb;

    let genome = genome_length();
    let y_max = points.iter().map(|p| p.y).fold(6.0_f64, f64::max).ceil() + 1.0;

    let colors = ["#4361ee", "#adb5bd"];
    let sig_line = -SIGNIFICANCE.log10();
    let sug_line = -SUGGESTIVE.log10();

    let mut svg = format!(
        r##"<svg viewBox="0 0 {w} {h}" xmlns="http://www.w3.org/2000/svg">"##
    );

    // Axes
    svg.push_str(&format!(
        r##"<line x1="{ml}" y1="{}" x2="{}" y2="{}" stroke="#ccc" stroke-width="0.5"/>"##,
        mt, ml + pw, mt
    ));
    svg.push_str(&format!(
        r##"<line x1="{ml}" y1="{}" x2="{ml}" y2="{mt}" stroke="#333" stroke-width="1"/>"##,
        mt + ph
    ));
    svg.push_str(&format!(
        r##"<line x1="{ml}" y1="{}" x2="{}" y2="{}" stroke="#333" stroke-width="1"/>"##,
        mt + ph, ml + pw, mt + ph
    ));

    // Y tick labels
    let mut y_tick = 0.0;
    while y_tick <= y_max {
        let py = mt + ph - (y_tick / y_max) * ph;
        svg.push_str(&format!(
            r##"<text x="{}" y="{}" text-anchor="end" font-size="9" fill="#555">{}</text>"##,
            ml - 6.0, py + 3.0, y_tick as i32
        ));
        svg.push_str(&format!(
            r##"<line x1="{ml}" y1="{py}" x2="{}" y2="{py}" stroke="#eee" stroke-width="0.5"/>"##,
            ml + pw
        ));
        y_tick += if y_max > 20.0 { 5.0 } else { 2.0 };
    }

    // Y axis label
    svg.push_str(&format!(
        r##"<text x="14" y="{}" transform="rotate(-90 14 {})" text-anchor="middle" font-size="10" fill="#333">&minus;log&#x2081;&#x2080;(p)</text>"##,
        mt + ph / 2.0, mt + ph / 2.0
    ));

    // Chromosome labels
    let offsets = chrom_offsets();
    for &(name, size) in CHROMS {
        if let Some(&(_, off)) = offsets.get(name) {
            let cx = ml + ((off as f64 + size as f64 / 2.0) / genome) * pw;
            let label = if CHROMS.len() > 20 && name.len() > 1 && name != "X" && name != "Y" {
                if name.parse::<u32>().unwrap_or(0) % 2 == 0 { name } else { "" }
            } else { name };
            if !label.is_empty() {
                svg.push_str(&format!(
                    r##"<text x="{cx}" y="{}" text-anchor="middle" font-size="8" fill="#777">{label}</text>"##,
                    mt + ph + 14.0
                ));
            }
        }
    }

    // X axis label
    svg.push_str(&format!(
        r##"<text x="{}" y="{}" text-anchor="middle" font-size="10" fill="#333">Chromosome</text>"##,
        ml + pw / 2.0, h - 4.0
    ));

    // Significance thresholds
    let sig_y = mt + ph - (sig_line / y_max) * ph;
    svg.push_str(&format!(
        r##"<line x1="{ml}" y1="{sig_y}" x2="{}" y2="{sig_y}" stroke="#c62828" stroke-width="0.8" stroke-dasharray="4 3"/>"##,
        ml + pw
    ));
    if sug_line < y_max {
        let sug_y = mt + ph - (sug_line / y_max) * ph;
        svg.push_str(&format!(
            r##"<line x1="{ml}" y1="{sug_y}" x2="{}" y2="{sug_y}" stroke="#ff9800" stroke-width="0.5" stroke-dasharray="2 3"/>"##,
            ml + pw
        ));
    }

    // Data points
    for p in points {
        let px = ml + (p.x / genome) * pw;
        let py = mt + ph - (p.y.min(y_max) / y_max) * ph;
        let color = colors[p.chrom_idx % 2];
        let r = if p.y >= sig_line { 3.0 } else { 1.8 };
        let opacity = if p.y >= sig_line { 0.9 } else { 0.5 };
        svg.push_str(&format!(
            r##"<circle cx="{px:.1}" cy="{py:.1}" r="{r}" fill="{color}" opacity="{opacity}"/>"##
        ));
    }

    // Labels for significant hits
    let mut labeled: Vec<&PlotPoint> = points.iter().filter(|p| p.y >= sig_line).collect();
    labeled.sort_by(|a, b| b.y.partial_cmp(&a.y).unwrap_or(std::cmp::Ordering::Equal));
    labeled.truncate(15);
    for p in &labeled {
        let px = ml + (p.x / genome) * pw;
        let py = mt + ph - (p.y.min(y_max) / y_max) * ph;
        svg.push_str(&format!(
            r##"<text x="{}" y="{}" font-size="7" fill="#c62828" text-anchor="start" transform="rotate(-35 {} {})">{}</text>"##,
            px + 3.0, py - 4.0, px + 3.0, py - 4.0, p.label
        ));
    }

    svg.push_str("</svg>");
    svg
}


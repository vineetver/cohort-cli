fn qq_svg(pvalues: &[f64]) -> String {
    let w = 360.0_f64;
    let h = 360.0_f64;
    let ml = 50.0; let mr = 15.0; let mt = 10.0; let mb = 45.0;
    let pw = w - ml - mr;
    let ph = h - mt - mb;

    let mut valid: Vec<f64> = pvalues.iter()
        .filter(|p| p.is_finite() && **p > 0.0 && **p <= 1.0)
        .copied()
        .collect();
    valid.sort_by(|a, b| a.partial_cmp(b).unwrap_or(std::cmp::Ordering::Equal));

    if valid.is_empty() {
        return r##"<svg viewBox="0 0 360 360" xmlns="http://www.w3.org/2000/svg"><text x="180" y="180" text-anchor="middle" font-size="12" fill="#999">No valid p-values</text></svg>"##.into();
    }

    let n = valid.len();
    let observed: Vec<f64> = valid.iter().map(|p| -p.log10()).collect();
    let expected: Vec<f64> = (0..n)
        .map(|i| -((i as f64 + 0.5) / n as f64).log10())
        .collect();

    let axis_max = observed.last().copied().unwrap_or(1.0)
        .max(*expected.last().unwrap_or(&1.0))
        .ceil() + 0.5;

    let colors = ["#4361ee", "#adb5bd"];

    let mut svg = format!(
        r##"<svg viewBox="0 0 {w} {h}" xmlns="http://www.w3.org/2000/svg">"##
    );

    // Identity line
    let x0 = ml; let y0 = mt + ph;
    let x1 = ml + pw; let y1 = mt;
    svg.push_str(&format!(
        r##"<line x1="{x0}" y1="{y0}" x2="{x1}" y2="{y1}" stroke="#ccc" stroke-width="1"/>"##
    ));

    // Axes
    svg.push_str(&format!(
        r##"<line x1="{ml}" y1="{}" x2="{ml}" y2="{mt}" stroke="#333" stroke-width="1"/>"##,
        mt + ph
    ));
    svg.push_str(&format!(
        r##"<line x1="{ml}" y1="{}" x2="{}" y2="{}" stroke="#333" stroke-width="1"/>"##,
        mt + ph, ml + pw, mt + ph
    ));

    // Ticks
    let tick_step = if axis_max > 20.0 { 5.0 } else if axis_max > 8.0 { 2.0 } else { 1.0 };
    let mut t = 0.0;
    while t <= axis_max {
        let px = ml + (t / axis_max) * pw;
        let py = mt + ph - (t / axis_max) * ph;
        svg.push_str(&format!(
            r##"<text x="{px}" y="{}" text-anchor="middle" font-size="9" fill="#555">{}</text>"##,
            mt + ph + 14.0, t as i32
        ));
        svg.push_str(&format!(
            r##"<text x="{}" y="{}" text-anchor="end" font-size="9" fill="#555">{}</text>"##,
            ml - 6.0, py + 3.0, t as i32
        ));
        t += tick_step;
    }

    // Axis labels
    svg.push_str(&format!(
        r##"<text x="{}" y="{}" text-anchor="middle" font-size="10" fill="#333">Expected &minus;log&#x2081;&#x2080;(p)</text>"##,
        ml + pw / 2.0, h - 4.0
    ));
    svg.push_str(&format!(
        r##"<text x="12" y="{}" transform="rotate(-90 12 {})" text-anchor="middle" font-size="10" fill="#333">Observed &minus;log&#x2081;&#x2080;(p)</text>"##,
        mt + ph / 2.0, mt + ph / 2.0
    ));

    // Points (subsample if too many)
    let step = if n > 2000 { n / 2000 } else { 1 };
    for i in (0..n).step_by(step) {
        let px = ml + (expected[i] / axis_max) * pw;
        let py = mt + ph - (observed[i] / axis_max) * ph;
        let color = colors[i % 2];
        svg.push_str(&format!(
            r##"<circle cx="{px:.1}" cy="{py:.1}" r="1.5" fill="{color}" opacity="0.5"/>"##
        ));
    }
    // Always draw the tail (most significant) without subsampling
    for i in n.saturating_sub(50)..n {
        let px = ml + (expected[i] / axis_max) * pw;
        let py = mt + ph - (observed[i] / axis_max) * ph;
        svg.push_str(&format!(
            r##"<circle cx="{px:.1}" cy="{py:.1}" r="2" fill="#c62828" opacity="0.8"/>"##
        ));
    }

    svg.push_str("</svg>");
    svg
}


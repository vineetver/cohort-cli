fn collect_pvalues(results: &[(MaskType, Vec<GeneResult>)]) -> Vec<f64> {
    results.iter()
        .flat_map(|(_, genes)| genes.iter().map(|g| g.staar.staar_o))
        .filter(|p| p.is_finite() && *p > 0.0 && *p <= 1.0)
        .collect()
}

#[cfg(test)]
mod tests {
    use super::*;
    use super::super::score_test::StaarResult;

    fn gene(chr: &str, start: u32, p: f64) -> GeneResult {
        GeneResult {
            ensembl_id: format!("ENSG{start}"),
            gene_symbol: format!("GENE{start}"),
            chromosome: chr.into(),
            start,
            end: start + 10000,
            n_variants: 5,
            cumulative_mac: 10,
            staar: StaarResult {
                burden_1_25: p * 2.0, burden_1_1: p * 3.0,
                skat_1_25: p * 1.5, skat_1_1: p * 2.5,
                acat_v_1_25: p * 1.2, acat_v_1_1: p * 2.0,
                per_annotation: Vec::new(),
                staar_b_1_25: p, staar_b_1_1: p,
                staar_s_1_25: p, staar_s_1_1: p,
                staar_a_1_25: p, staar_a_1_1: p,
                acat_o: p, staar_o: p,
            },
        }
    }

    #[test]
    fn manhattan_svg_renders() {
        let results = vec![
            (MaskType::PLof, vec![
                gene("1", 50_000_000, 1e-8),
                gene("2", 100_000_000, 0.05),
                gene("22", 30_000_000, 1e-3),
            ]),
        ];
        let svg = manhattan_svg(&layout_points(&results));
        assert!(svg.contains("<svg"));
        assert!(svg.contains("</svg>"));
        assert!(svg.contains("circle"));
    }

    #[test]
    fn qq_svg_renders() {
        let pvals = vec![0.5, 0.1, 0.01, 0.001, 1e-6, 1e-10];
        let svg = qq_svg(&pvals);
        assert!(svg.contains("<svg"));
        assert!(svg.contains("circle"));
    }

    #[test]
    fn top_genes_filters_significant() {
        let results = vec![
            (MaskType::PLof, vec![
                gene("1", 100, 1e-8),
                gene("1", 200, 0.5),
            ]),
        ];
        let hits = top_genes(&results, 2.5e-6);
        assert_eq!(hits.len(), 1);
        assert!(hits[0].staar_o < 2.5e-6);
    }

    #[test]
    fn empty_results_no_panic() {
        let results: Vec<(MaskType, Vec<GeneResult>)> = Vec::new();
        let svg = manhattan_svg(&layout_points(&results));
        assert!(svg.contains("</svg>"));
        let qq = qq_svg(&[]);
        assert!(qq.contains("No valid p-values"));
    }
}

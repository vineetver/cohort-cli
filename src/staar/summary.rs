struct HitRecord {
    mask: String,
    gene: String,
    chromosome: String,
    n_variants: u32,
    cumulative_mac: u32,
    staar_o: f64,
    burden: f64,
    skat: f64,
    acat_v: f64,
}

fn top_genes(results: &[(MaskType, Vec<GeneResult>)], threshold: f64) -> Vec<HitRecord> {
    let mut hits: Vec<HitRecord> = results.iter().flat_map(|(mask, genes)| {
        genes.iter().filter(|g| g.staar.staar_o < threshold).map(|g| HitRecord {
            mask: mask.file_stem(),
            gene: g.gene_symbol.clone(),
            chromosome: g.chromosome.clone(),
            n_variants: g.n_variants,
            cumulative_mac: g.cumulative_mac,
            staar_o: g.staar.staar_o,
            burden: g.staar.burden_1_25,
            skat: g.staar.skat_1_25,
            acat_v: g.staar.acat_v_1_25,
        })
    }).collect();
    hits.sort_by(|a, b| a.staar_o.partial_cmp(&b.staar_o).unwrap_or(std::cmp::Ordering::Equal));
    hits
}

fn hits_table(hits: &[HitRecord]) -> String {
    if hits.is_empty() {
        return "<p style='color:#888;margin-top:1rem;'>No genome-wide significant genes.</p>".into();
    }

    let mut html = String::from(
        r#"<div class="plot" style="overflow-x:auto;">
<h3 style="font-size:0.85rem;margin-bottom:0.5rem;">Significant Genes (STAAR-O &lt; 2.5×10⁻⁶)</h3>
<table>
<thead><tr>
<th>Mask</th><th>Gene</th><th>Chr</th><th>Variants</th><th>cMAC</th>
<th>STAAR-O</th><th>Burden</th><th>SKAT</th><th>ACAT-V</th>
</tr></thead><tbody>"#
    );

    for h in hits {
        html.push_str(&format!(
            "<tr><td>{}</td><td><b>{}</b></td><td>{}</td><td>{}</td><td>{}</td>\
             <td class='sig'>{}</td><td>{}</td><td>{}</td><td>{}</td></tr>",
            h.mask, h.gene, h.chromosome, h.n_variants, h.cumulative_mac,
            fmt_p(h.staar_o), fmt_p(h.burden), fmt_p(h.skat), fmt_p(h.acat_v),
        ));
    }

    html.push_str("</tbody></table></div>");
    html
}

fn fmt_p(p: f64) -> String {
    if p.is_nan() { "—".into() }
    else if p < 1e-300 { "&lt;1e-300".into() }
    else { format!("{:.2e}", p) }
}

